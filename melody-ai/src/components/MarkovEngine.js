// ─── pickNextDuration ─────────────────────────────────────────────────────────

export function pickNextDuration(last2, last1, mood) {
  const key  = `${last2},${last1}`;
  const base = RHYTHM_TRANSITIONS[key] ?? RHYTHM_TRANSITIONS['0.5,0.5'];

  // Shallow copy so we don't mutate the table
  const table = { ...base };

  // Apply mood multipliers
  switch (mood) {
    case 'happy':
      if (table['0.25'] !== undefined) table['0.25'] *= 1.4;
      break;
    case 'sad':
      if (table['1.0']  !== undefined) table['1.0']  *= 1.6;
      if (table['0.75'] !== undefined) table['0.75'] *= 1.3;
      break;
    case 'epic':
      if (table['0.25'] !== undefined) table['0.25'] *= 1.3;
      if (table['1.0']  !== undefined) table['1.0']  *= 1.3;
      break;
    case 'lofi':
      if (table['0.75'] !== undefined) table['0.75'] *= 1.5;
      if (table['1.0']  !== undefined) table['1.0']  *= 1.2;
      break;
    case 'jazz':
      if (table['0.25'] !== undefined) table['0.25'] *= 1.2;
      table['0.375'] = (table['0.375'] ?? 0.1) * 1.8;
      break;
    case 'classical':
      if (table['0.5']  !== undefined) table['0.5']  *= 1.4;
      break;
    case 'romantic':
      if (table['0.75'] !== undefined) table['0.75'] *= 1.5;
      if (table['1.0']  !== undefined) table['1.0']  *= 1.3;
      break;
    case 'mysterious': {
      if (table['1.0']  !== undefined) table['1.0']  *= 1.5;
      // Add rest entry so that after normalization it has ~30% probability
      const currentSum = Object.values(table).reduce((a, b) => a + b, 0);
      table['0'] = (0.3 / 0.7) * currentSum;
      break;
    }
    default:
      break;
  }

  // Renormalize
  const total   = Object.values(table).reduce((a, b) => a + b, 0);
  const entries = Object.entries(table).map(([k, v]) => [parseFloat(k), v / total]);

  // Weighted sample
  let r = Math.random();
  for (const [duration, prob] of entries) {
    r -= prob;
    if (r <= 0) return duration;
  }
  return entries[entries.length - 1][0];
}

// ─── pickNextChord ────────────────────────────────────────────────────────────

export function pickNextChord(currentDegree, mood) {
  const moodTable = CHORD_TRANSITIONS[mood] ?? CHORD_TRANSITIONS.happy;
  const table     = moodTable[currentDegree] ?? moodTable[0] ?? { 0:0.4, 3:0.3, 4:0.3 };
  const entries   = Object.entries(table).map(([k, v]) => [parseInt(k, 10), v]);
  const total     = entries.reduce((a, [, v]) => a + v, 0);

  let r = Math.random();
  for (const [degree, prob] of entries) {
    r -= prob / total;
    if (r <= 0) return degree;
  }
  return entries[entries.length - 1][0];
}

// ─── generateChordSequence ────────────────────────────────────────────────────

export function generateChordSequence(mood, slotCount) {
  const sequence = [];
  let current    = 0; // always open on tonic
  for (let i = 0; i < slotCount; i++) {
    current = pickNextChord(current, mood);
    sequence.push(current);
  }
  return sequence;
}

